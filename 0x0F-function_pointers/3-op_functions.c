#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - operation
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - operation
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - operation
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - operation
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - operation
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
