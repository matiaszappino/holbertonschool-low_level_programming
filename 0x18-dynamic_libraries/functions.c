/**
 * add - add a + b
 * @a: integer a
 * @b: integer b
 * Return: returns the result of a + b
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub a - b
 * @a: integer a
 * @b: integer b
 * Return: returns the result of a - b
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul a * b
 * @a: integer a
 * @b: integer b
 * Return: returns the result of a * b
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - div a / b
 * @a: integer a
 * @b: integer b
 * Return: returns the result of a / b
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod a % b
 * @a: integer a
 * @b: integer b
 * Return: returns the result of a % b
*/
int mod(int a, int b)
{
	return (a % b);
}
